package entities;

public class Clientes {
	private int id_cliente;
	private int id_usurario;
	private String nacionalidad;
	private String pasaporte;
	
	
	public int getId_cliente() {
		return id_cliente;
	}
	public void setId_cliente(int id_cliente) {
		this.id_cliente = id_cliente;
	}
	public int getId_usurario() {
		return id_usurario;
	}
	public void setId_usurario(int id_usurario) {
		this.id_usurario = id_usurario;
	}
	public String getNacionalidad() {
		return nacionalidad;
	}
	public void setNacionalidad(String nacionalidad) {
		this.nacionalidad = nacionalidad;
	}
	public String getPasaporate() {
		return pasaporte;
	}
	public void setPasaporate(String pasaporate) {
		this.pasaporte = pasaporate;
	}
	
	
	
}
